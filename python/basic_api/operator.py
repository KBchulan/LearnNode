# 这一部分比较简单，主要以实例为主，我们只展示与cc不太一样的地方（其实cc通过operator啥都能干）


# 基础运算符号
# +
print(1 + 2.2)
print("hello " + "operator!")
print(True + False)

# - 

# *
print("八奈见杏菜 " * 4)

# /（返回float）

# **（指数）
print(16**(1/4))

# //（整除）
print(6 // 4)
print(6.0 // 4)

# %（取模）
print(3 % -4)


# 比较运算符号
# > >= < <= == !=
print("hello" < "world")

# 链式比较运算符号
print(1 < 5 < 10)


# 逻辑运算符
# and or not
print(True and False)
print(True or False)
print(not True)


# 赋值操作符
# = += -= *= /= %= //= **=


# 成员操作符(T or F)
# in not in
print(1 in (1, 2, 3))
print(1 not in (1, 2, 3))